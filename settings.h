#ifndef _SETTINGS_H_
#define _SETTINGS_H_

/*******************************************************************************
 * Timer modes.
 ******************************************************************************/
enum class t_mode : uint8_t
{
	NONE	= 0,
	NORMAL	= 1,
	CTC	= 2
};

/*******************************************************************************
 * Interrupts only in NORMAL or CTC.
 ******************************************************************************/
enum class t_interrupt : uint8_t
{
	NONE	= 0,
	OVF	= 1,
	COMPA	= 2,
	COMPB	= 3
	//COMPC = 4;
	//CAPT  = 5;
};

/*******************************************************************************
 * Types only in PWM.
 ******************************************************************************/
enum class t_pwm : uint8_t
{
	NONE	= 0,
	FAST	= 1,
	PC	= 2
};

#endif
